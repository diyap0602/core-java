package corejava;
/**
* 
* Using while loop for print the 1 to 10 number.
* 
* @author Diya
**/
public class Loop {

	public static void main(String[] args) {
		// Initialize the start number.
		int startNumber = 1;
		while (startNumber<=10) {
			System.out.println(startNumber);
			startNumber++;
		}
	}

}